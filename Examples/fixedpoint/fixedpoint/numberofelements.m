function n = numberofelements(a)
%NUMBEROFELEMENTS Number of elements in an array
%   N = numberofelements(A) returns the number of elements, N, in array A.
%    
%   Note: NUMBEROFELEMENTS will be removed in a future release. Use NUMEL instead.    
%
%   See also NUMEL

%   Copyright 2012-2013 The MathWorks, Inc.

n = numel(a);

end
