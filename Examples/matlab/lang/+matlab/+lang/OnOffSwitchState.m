%matlab.lang.OnOffSwitchState Represent off and on states with logical values
%  matlab.lang.OnOffSwitchState enumerates the states of a simple switch with 
%  values on and off.  The on and off values are compatible with logical arrays 
%  where off is 0 and on is 1 and can be used in logical expressions such
%  as if-statements and while loops.
%
%  OnOffSwitchState enumeration members:
%    off - represent an off state, or logical 0 (false)
%    on  - represent an on state, or logical 1 (true)

