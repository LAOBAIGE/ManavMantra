%PRECEDENCE Operator Precedence in MATLAB.
%
%   MATLAB has the following precedence for the built-in operators when
%   evaluating expressions (from highest to lowest):
%
%      1. parentheses ()
%
%      2. transpose (.'), power (.^), complex conjugate
%         transpose ('), matrix power (^)
%
%      3. power with unary minus (.^-), unary plus (.^+), or logical
%         negation (.^~) as well as matrix power with unary minus (^-), 
%         unary plus (^+), or logical negation (^~)
%
%      4. unary plus (+), unary minus (-), logical negation (~)
%
%      5. multiplication (.*), right division (./), left
%         division (.\), matrix multiplication (*), matrix right 
%         division (/), matrix left division (\)
%
%      6. addition (+), subtraction (-)
%
%      7. colon operator (:)
%
%      8. less than (<), less than or equal to (<=), greater than
%         (>), greater than or equal to (>=), equal to (==), not
%         equal to (~=)
%
%      9. element-wise logical AND (&)
%
%     10. element-wise logical OR (|)
%
%     11. short-circuit logical AND (&&)
%
%     12. short-circuit logical OR (||)
%
%   See also SYNTAX, ARITH.

