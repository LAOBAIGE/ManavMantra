%DBTYPE  行番号付きの M-ファイルを表示
%
%   DBTYPE は、ブレークポイントを設定しやすいように、行番号を付けて M-ファイル
%   関数を表示します。このコマンドには、以下の 2 つの形式があります。
%   以下のようになります。
%
%   DBTYPE MFILE
%   DBTYPE MFILE start:end
%
%   ここで、MFILE は M-ファイル関数名で、start と end は行番号です。
%
%   DBTYPE FILENAME は、絶対パス名または MATLABPATH の相対部分パス名を持つ
%   ファイルの内容を表示します (PARTIALPATH を参照)。
%
%   参考 DBSTEP, DBSTOP, DBCONT, DBCLEAR, DBSTACK, DBUP, DBDOWN,
%        DBSTATUS, DBQUIT, PARTIALPATH, TYPE.


