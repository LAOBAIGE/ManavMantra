%DBQUIT  デバッグモードの終了
%
%   DBQUIT は、デバッグモードを終了します。コマンドウィンドウは、その後、
%   標準のプロンプト (>>) を表示します。処理中の M-ファイルは終了せず、
%   結果は返されません。すべてのブレークポイントは有効のままです。
%
%   MATLAB が複数の関数に対してデバッグモードにある場合、DBQUIT は、
%   現在停止している MATLAB の関数に対してデバッグを終了し、コントロールは
%   デバッグ中の別の関数に移ります。
%
%   参考 DBSTOP, DBCONT, DBSTEP, DBCLEAR, DBTYPE, DBSTACK, DBUP, DBDOWN, 
